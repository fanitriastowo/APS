    private void kelasLookup() {
        cmbKelas.removeAllItems();
        KelasService kelasService = new KelasServiceImpl();
        List<Kelas> kelasList = kelasService.findAll();
        for (Kelas kelas : kelasList) {
            cmbKelas.addItem(kelas.getNama_kelas());
        }
    }
    
    private void prepareCetak(){
        if (radAngsuran.isSelected()) {
            cetakAngsuran();
        } else if (radTunai.isSelected()){
            cetakTunai();
        } else if (radBulanan.isSelected()){
            cetakBulanan();
        }
    }

