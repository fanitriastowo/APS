    private void cetakTunai() {
        try {
            String path = "/com/yf/kp/report/tagihan/fix/LaporanTagihanTunai.jasper";
            HashMap map = new HashMap();
            map.put("kelas", cmbKelas.getSelectedItem().toString());
            map.put("namaTagihan", cmbNamaTagihan.getSelectedItem().toString());
            JasperPrint jasperPrint = JasperFillManager.fillReport(getClass().getResourceAsStream(path), map, Dbconnection.connect());
            JasperViewer.viewReport(jasperPrint, false);
            this.dispose();
        } catch (ClassNotFoundException | JRException ex) {
            Logger.getLogger(ReportSiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
